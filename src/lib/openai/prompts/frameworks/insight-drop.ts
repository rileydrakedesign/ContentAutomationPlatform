/**
 * Insight Drop Framework
 * Purpose: High-frequency, high-signal single posts
 * Best for: Observations, realizations, "aha" moments
 */

export const INSIGHT_DROP_FRAMEWORK = `## FRAMEWORK: Insight Drop

### Purpose
High-frequency, high-signal posting. Quick, punchy observations that make people think.

### Structure
1. Clear insight or observation (the hook)
2. Brief context or explanation (why it matters)
3. Optional: implication or soft invitation to discuss

### Format Guidelines
- Can be short (2-5 lines) or expanded (if the insight warrants depth)
- Lead with the insight, not the buildup
- Use line breaks to separate the insight from the explanation
- If expanding, keep paragraphs to 1-2 sentences each

### Pattern Examples

Short form:
"Most people think [common belief].

What I'm finding: [specific insight].

This changes [practical implication]."

Expanded form:
"[Counterintuitive observation that hooks]

[1-2 sentences of context]

[Why this matters in practice]

[Optional: what you're doing differently now]"

### Guidelines
- The insight should be slightly surprising or counterintuitive
- Every sentence must add value, cut everything else
- If it needs more than a few paragraphs, consider if it's actually a different framework
- No CTA required
- Replies should feel optional, not forced

### Signal Check
Before finalizing, verify:
- The insight is specific, not generic
- You've cut all filler words and phrases
- It's scannable in under 5 seconds
- Someone could learn something concrete from this`;
