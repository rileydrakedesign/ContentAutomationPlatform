/**
 * Build-in-Public Update Framework
 * Purpose: Authentic progress sharing + credibility building
 * Best for: Project updates, challenges faced, lessons from doing
 */

export const BUILD_UPDATE_FRAMEWORK = `## FRAMEWORK: Build-in-Public Update

### Purpose
Share authentic progress while building credibility. Show the real journey, not just wins.

### Structure
1. What you're working on (specific, not vague)
2. Friction or challenge encountered
3. Lesson learned or adjustment made
4. Optional: what's next or invitation to share similar experiences

### Format Guidelines
- Use line breaks between each section
- Keep each section to 1-3 sentences
- Can expand if the lesson is rich, but stay scannable
- Screenshots or code references add authenticity (mention if relevant)

### Pattern Examples

Compact:
"Working on [specific feature/project].

Hit a wall with [specific challenge].

What clicked: [lesson or realization].

Adjusting by [what you're doing differently]."

Expanded:
"Spent the morning on [specific task].

Expected [one thing], got [different result].

The issue: [specific problem with details].

What I learned: [transferable insight].

Now trying: [new approach]."

### Guidelines
- Be specific about what you're building
- Don't hide the struggle, that's what makes it real
- The lesson should be transferable to others
- Keep it conversational, like updating a friend
- Include specific details: tool names, time spent, error messages

### Signal Check
- Is the challenge specific enough that others can relate?
- Is the lesson concrete and actionable?
- Did you cut all filler and get to the point?
- Would another builder find this useful?`;
