export const OPINION_FRAMEWORK = `## FRAMEWORK: Opinion with Reasoning

### Purpose
Share a clear perspective while demonstrating nuanced thinking. Thought leadership without being inflammatory.

### Structure
1. Clear opinion (state your position upfront)
2. Reasoning (why you think this, from experience)
3. Nuance (when this doesn't apply or counterargument)
4. Optional: invitation to discuss or hear other perspectives

### Format Guidelines
- Lead with the opinion, don't bury it
- Separate the opinion from reasoning with line breaks
- Keep reasoning grounded in specifics, not abstractions
- The nuance shows you've thought it through

### Pattern Examples

Compact:
"[Clear opinion statement]

Why: [1-2 sentences of reasoning from experience]

Though: [acknowledgment of when this doesn't apply]"

Expanded:
"I don't think [popular take] is the right framing.

What I'm seeing instead: [your perspective]

This comes from [specific experience or observation].

That said: [genuine acknowledgment of other side]

Curious what others are finding."

### Guidelines
- Your reasoning should come from experience, not theory
- Acknowledge the other side genuinely, not dismissively
- Be opinionated but not absolute
- Invite discussion, not argument
- Avoid tribal signaling or dunking

### Tone
- Confident but curious
- "Here's what I think" not "Here's the truth"
- Willing to be wrong
- Respectful of other perspectives

### Signal Check
- Is the opinion clear in the first line?
- Is reasoning based on your actual experience?
- Have you acknowledged legitimate counterpoints?
- Would this start a conversation or a fight?`;
